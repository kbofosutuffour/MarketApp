function publish() {
    let p = document.getElementById('publish');
    p.style.color = "rgb(84, 166, 237)";
   
}
function publish_out() {
    let p = document.getElementById('publish');
    p.style.color = "gray";
}

function cancel() {
    let p = document.getElementById('cancel');
    p.style.color = "red";
   
}
function cancel_out() {
    let p = document.getElementById('cancel');
    p.style.color = "gray";
}

